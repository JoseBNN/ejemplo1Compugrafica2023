    public String listarClientes(LinkedList<Cliente> datos) {
        String res = "Los Clientes son:\n";

        for (Cliente obj : datos) {
            // verifica si el objeto de la posicion i es de la clase PubicacionDigital

            res += "Nombre del Cliente:" + obj.getNombreP()
                    + "  Numero de telefono:" + obj.getTelefonoP()
                    + "\n Correo cliente " + obj.getCorreoP()
                    + "Edad Cliente" + obj.getEdadCliente()
                    + "Codigo Cliente" + obj.getCodigoCliente();
        }

        return res;
    }

    public void almacenarArchivoObjetos(LinkedList<Cliente> bd) {
        // Actualizar el nombre del archivo
        String nombreArchivo = "listarCliente.obj";

        ObjectOutputStream salida = null;
        try {
            // escritura de datos
            salida = new ObjectOutputStream(
                    new FileOutputStream(nombreArchivo));

            // adicionar instruccion para escribir los datos
            salida.writeObject(bd);
        } catch (IOException e) {
            System.out.println("Error almacenando los datos " + e.getMessage());
        } finally {
            try {
                salida.close();
            } catch (IOException e) {
                System.out.println("Error cerrando ");
            }
        }
    }

